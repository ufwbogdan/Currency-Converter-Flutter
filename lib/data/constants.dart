// the main converting function
double convertCurrency({
  required double amount,
  required String from,
  required String to,
}) {
  double? fromRate = exchangeRatesNotifier.value[from];
  double? toRate = exchangeRatesNotifier.value[to];

  // null checks if the API failed to fetch
  fromRate ??= exchangeRates[from];
  toRate ??= exchangeRates[to];

  if (fromRate == null || toRate == null) {
    return 0.0;
  }

  return amount / fromRate * toRate;
}

